import {readdir,readFile,writeFile} from "node:fs/promises";
import {relative,resolve,sep} from "node:path";

const root=resolve(process.cwd(),"app");
const output=resolve(root,"lib/generated-capability-manifest.ts");
const ignored=new Set(["layout.tsx","loading.tsx","error.tsx","not-found.tsx"]);

async function walk(directory){
  const entries=await readdir(directory,{withFileTypes:true});
  const files=[];
  for(const entry of entries){
    const full=resolve(directory,entry.name);
    if(entry.isDirectory())files.push(...await walk(full));
    else if(!ignored.has(entry.name)&&(entry.name==="page.tsx"||entry.name==="route.ts"))files.push(full);
  }
  return files;
}

function routeFor(file){
  const parts=relative(root,file).split(sep).slice(0,-1);
  return "/"+parts.map(part=>part.startsWith("[")?`:${part.slice(1,-1)}`:part).join("/");
}

const files=await walk(root);
const routes=[];
for(const file of files){
  const source=await readFile(file,"utf8");
  const kind=file.endsWith("page.tsx")?"page":"api";
  const methods=kind==="api"?[...source.matchAll(/export\s+(?:async\s+)?function\s+(GET|POST|PUT|PATCH|DELETE)\b/g)].map(match=>match[1]):["GET"];
  routes.push({route:routeFor(file),kind,methods:[...new Set(methods)].sort(),source:relative(process.cwd(),file).split(sep).join("/")});
}
routes.sort((a,b)=>a.route.localeCompare(b.route)||a.kind.localeCompare(b.kind));
let generatedAt=new Date().toISOString();
try{
  const previous=await readFile(output,"utf8");
  const previousDate=previous.match(/CAPABILITY_MANIFEST_GENERATED_AT=("[^"]+")/)?.[1];
  const previousRoutes=previous.match(/GENERATED_ROUTE_CAPABILITIES=([\s\S]+) as const;\s*$/)?.[1];
  if(previousDate&&previousRoutes&&JSON.stringify(JSON.parse(previousRoutes))===JSON.stringify(routes))generatedAt=JSON.parse(previousDate);
}catch{/* First generation intentionally uses the current build time. */}
const body=`/* Generated by scripts/generate-capability-manifest.mjs. Do not edit by hand. */\nexport const CAPABILITY_MANIFEST_GENERATED_AT=${JSON.stringify(generatedAt)};\nexport const GENERATED_ROUTE_CAPABILITIES=${JSON.stringify(routes,null,2)} as const;\n`;
await writeFile(output,body);
console.log(`Capability manifest: ${routes.length} routes`);
